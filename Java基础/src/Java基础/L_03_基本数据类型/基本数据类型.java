package Java基础.L_03_基本数据类型;

public class 基本数据类型 {
    public static void main(String[] args) {
        // 布尔
        boolean a = false;

        // 字符
        char c = 'c';

        // 数值型（整型）
        byte b = 1; // 1
        short d = 250; // 2
        int e = 50;  // 4
        long f = 13; // 8

        // 数值型（浮点型） 浮点型其实无法精确的表示小数，因此浮点数表示的是实数的近似值
        float h = 3.0F; //
        double i = 5.0;

    }
}
